#include "cub3d.h"

static char	*get_map[] = {
	"1111111111111111111111111",
	"1000000000000000000000001",
	"1000000001000001000000001",
	"1000000001000001000000001",
	"1000000001000001000000001",
	"1000000000000000000000001",
	"1000000000000000000000001",
	"1000000000000000000000001",
	"1000000000000000000000001",
	"1000000000000000000000001",
	"1000000000000000000000001",
	"1111111111111111111111111",
	NULL
};

// Allocates and fills a hardâ€‘coded test map
void	init_test_map(t_app *app)
{
	int		i;

	app->map = malloc(sizeof(t_map));
	if (!app->map)
		return ;
	app->map->width = 25;
	app->map->height = 12;
	app->map->grid = malloc(sizeof(char *) * (app->map->height + 1));
	if (!app->map->grid)
		return (free(app->map), (void)0);
	i = 0;
	while (i < app->map->height)
	{
		app->map->grid[i] = ft_strdup(get_map[i]);
		if (!app->map->grid[i])
		{
			while (--i >= 0)
				free(app->map->grid[i]);
			free(app->map->grid);
			free(app->map);
			app->map = NULL;
			return ;
		}
		i++;
	}
	app->map->grid[i] = NULL;
}
